
class Estudiante {

    constructor(id,nombre,apellido,edad,telefono,encargado,semestre,anio,estado){

        this.id = id;
        this.nombre = nombre;
        this.apellido = apellido;
        this.edad = edad;
        this.telefono = telefono;
        this.encargado = encargado;
        this.semestre = semestre;
        this.anio = anio;
        this.estado = estado;

    }

}

    module.exports = Estudiante;